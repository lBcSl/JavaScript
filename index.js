function primeiraFuncao(a, b) {

    return soma = a + b;
}

function segundaFuncao(a, b) {

    return subtrai = a - b;
}

function terceiraFuncao(a, b) {

    return multiplica = a * b;

}

function quartaFuncao(a, b) {

    return divide = a / b;
}

function resultados(a, b) {
    console.log("Soma: " + primeiraFuncao(a, b));
    console.log("Subtração: " + segundaFuncao(a, b));
    console.log("Multiplicação: " + terceiraFuncao(a, b));
    console.log("Divisão: " + quartaFuncao(a, b));
}

resultados(2, 4);

